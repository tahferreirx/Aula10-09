package Atividades;

public class Cidade {
	
	private String nome;
	private Estado estado;
	
	protected String getNome() {
		return nome;
	}
	protected void setNome(String nome) {
		this.nome = nome;
	}
	protected Estado getEstado() {
		return estado;
	}
	protected void setEstado(Estado estado) {
		this.estado = estado;
	}

}
