package Atividades;

class Estado {
	
	private String nome;
	private String sigla;
	
	protected String getNome() {
		return nome;
	}
	protected void setNome(String nome) {
		this.nome = nome;
	}
	protected String getSigla() {
		return sigla;
	}
	protected void setSigla(String sigla) {
		this.sigla = sigla;
	}
	
}
